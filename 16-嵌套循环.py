# 9*9乘法表
e = 1
f = 1
while e <= 9:  # 行
    while f <= e:  # 每行算式的个数等于这一行的行号（第一行一个，第二行两个）
        print(f'{f}*{e}={f*e}', end='\t')
        f += 1
    print()
    f = 1  # 初始化
    e += 1
