day = 1
time = 1
while day <= 7:  # 判断天数（第一层循环）
    print(f'第{day}天')
    while time <= 3:  # 判断时间（第二层循环）
        print(f'今天是第{day}天', end='')
        if   time == 1:
            print('的早上')
            time += 1
        elif time == 2:
            print('的中午')
            time += 1
        else:
            print('的晚上')
            time = 1  # 需要初始化
            break  # 跳出循环
    day += 1
